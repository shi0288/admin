package com.xyauto.qa.cons;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

/**
 * Created by shiqm on 2017/3/9.
 */

@Component
@ConfigurationProperties(prefix = "xyauto.qa.user")
public class ApiUserCons {

    private String app_id;
    private String sign;
    private String server_user_name;
    private String server_qa_name;
    private String server_qa_task;

    public String getApp_id() {
        return app_id;
    }

    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }

    public String getServer_user_name() {
        return server_user_name;
    }

    public void setServer_user_name(String server_user_name) {
        this.server_user_name = server_user_name;
    }

    public String getServer_qa_name() {
        return server_qa_name;
    }

    public void setServer_qa_name(String server_qa_name) {
        this.server_qa_name = server_qa_name;
    }

	public String getServer_qa_task() {
		return server_qa_task;
	}

	public void setServer_qa_task(String server_qa_task) {
		this.server_qa_task = server_qa_task;
	}
    
}
