package com.xyauto.qa.entity;

import javax.persistence.*;

@Table(name = "question_car")
public class QuestionCar {
    @Id
    @Column(name = "question_car_id")
    private Long questionCarId;

    @Column(name = "question_id")
    private Long questionId;

    @Column(name = "brand_id")
    private Integer brandId;

    @Column(name = "subbrand_id")
    private Integer subbrandId;

    @Column(name = "series_id")
    private Integer seriesId;

    @Column(name = "created_at")
    private Integer createdAt;

    /**
     * @return question_car_id
     */
    public Long getQuestionCarId() {
        return questionCarId;
    }

    /**
     * @param questionCarId
     */
    public void setQuestionCarId(Long questionCarId) {
        this.questionCarId = questionCarId;
    }

    /**
     * @return question_id
     */
    public Long getQuestionId() {
        return questionId;
    }

    /**
     * @param questionId
     */
    public void setQuestionId(Long questionId) {
        this.questionId = questionId;
    }

    /**
     * @return brand_id
     */
    public Integer getBrandId() {
        return brandId;
    }

    /**
     * @param brandId
     */
    public void setBrandId(Integer brandId) {
        this.brandId = brandId;
    }

    /**
     * @return subbrand_id
     */
    public Integer getSubbrandId() {
        return subbrandId;
    }

    /**
     * @param subbrandId
     */
    public void setSubbrandId(Integer subbrandId) {
        this.subbrandId = subbrandId;
    }

    /**
     * @return series_id
     */
    public Integer getSeriesId() {
        return seriesId;
    }

    /**
     * @param seriesId
     */
    public void setSeriesId(Integer seriesId) {
        this.seriesId = seriesId;
    }

    /**
     * @return created_at
     */
    public Integer getCreatedAt() {
        return createdAt;
    }

    /**
     * @param createdAt
     */
    public void setCreatedAt(Integer createdAt) {
        this.createdAt = createdAt;
    }
}